    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name=name_field.getText();
        String writer=writer_field.getText();
        String type=type_field.getText();
        String publisher=publisher_field.getText();
        
        int selectedrow=book_table.getSelectedRow();
        
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                message2.setText("Herhangi bir kitap secili degil!");
            }
            else{
                message2.setText("Herhangi bir kitap secili degil!");
            }
        }
        else{
            int id=(int)model.getValueAt(selectedrow, 0);
            op.bookUpdate(id,name,writer,type,publisher);
            bookView();
            message1.setText("Kitap basarıyla düzenlendi");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        message1.setText("");
        int selectedrow=book_table.getSelectedRow();
        
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                message1.setText("Herhangi bir kitap secili degil!");
            }
            else{
                message1.setText("Silinecek kitap seçilmedi");
            }
        }
        else{
            int id=(int)model.getValueAt(selectedrow, 0);
            op.bookDelete(id);
            bookView();
            message1.setText("Silme işlemi Başarılı");
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

